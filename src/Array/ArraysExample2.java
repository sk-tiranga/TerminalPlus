package Array;

public class ArraysExample2 {

	public static void main(String[] args) {
		
		String s[] = new String[3];
		s[0]="Welcome";
		s[1]="To";
		s[2]="India";
		
		
		
		System.out.println(s.length);//returns length of an array
		
		for(String i:s)
		{
			System.out.println(i);
		}  
	}

}
